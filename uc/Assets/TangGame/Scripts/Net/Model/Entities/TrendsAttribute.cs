using System;
using System.Collections.Generic;

namespace TangGame.Net
{
/// <summary>
/// Description of TrendsAttribute.
/// </summary>
public class TrendsAttribute
{
    /** 附加强壮属性 */
    public short strength;
    /** 附加体质属性 */
    public short stamina;
    /** 附加敏捷属性 */
    public short agility;
    /** 附加悟性属性 */
    public short savvy;

    /** 附加HP上限 */
    public int addMaxHP;
    /** HP上限增加百分比 */
    public double addAdditionMaxHP;
    /** 附加MP上限 */
    public int addMaxMP;

    /** 附加最小攻击 */
    public int addMinAttack;
    /** 附加最大攻击 */
    public int addMaxAttack;
    /** 附加攻击加成 */
    public double addAdditionAttack;
    /** 技能伤害加成 */
    public double addSkillHurt;
    /** 附加防御 */
    public int addDefence;
    /** 附加防御加成 */
    public double addAdditionDefence;
    /** 附加暴击伤害百分比 */
    public double addDritical;
    /** 附加暴击值 */
    public int addDriticalValue;
    /** 附加闪避值 */
    public int addEvadeValue;
    /** 附加命中值 */
    public int addHitValue;

    /** 减少伤害比例 */
    public double reduceHurt;
    /** 固定追加伤害(绝对伤害) */
    public int absoluteAttack;
    /** 绝对防御(固定减伤) */
    public int absoluteDefense;

    /** 伤害反击 */
    public int strikeBack;
    /** 吸取生命 */
    public int assimilateHP;
    /** 降低MP消耗 */
    public int lowerMpConsume;
    /** 无视暴击几率 */
    public double ignoreDriticalOdds;
    /** 忽略闪避几率 */
    public double ignoreDodgeOdds;
    /** debuff无效几率 */
    public double debuffInvalidOdds;
    /** 降低流血效果伤害 */
    public int reduceBleedHurt;
    /** 转换伤害比例 */
    public double transformHurt;
    /** 减少被定身时间 */
    public int reduceFreezeTime;
    /** 被定身结束时间 */
    public bool isFreeze;
    /** 使目标定身附加时长 */
    public int makeFreezeTime;
    /** 使目标流血附加值 */
    public int makeBleed;

    /** 经验加成比例 */
    public float addExp;
    /** 生命恢复值 */
    public int hpRegainValue;
    /** 体力恢复值 */
    public int mpRegainValue;

    /** 暴击几率 */
    public double driticalOdds;
    /** 闪避几率 */
    public double evadeOdds;

    //==============================================================//
    /** 总共力量 */
    public short totalStrength;
    /** 总共体力 */
    public short totalStamina;
    /** 总共敏捷 */
    public short totalAgility;
    /** 总共悟性 */
    public short totalSavvy;

    /** 生命上限 */
    public int totalHpMax;
    /** 体力上限 */
    public int totalMpMax;
    /** 最小攻击值 */
    public int totalMinAttack;
    /** 最大攻击值 */
    public int totalMaxAttack;
    /** 攻击加成 */
    public double totalAdditionAttack;
    /** 防御 */
    public int totalDefence;
    /** 暴击值 */
    public int totalDriticalValue;
    /** 闪避值 */
    public int totalEvadeValue;
    //==============================================================//
    /** 套装数量 */
    public Dictionary<int, int> suitNum = new Dictionary<int, int>();

