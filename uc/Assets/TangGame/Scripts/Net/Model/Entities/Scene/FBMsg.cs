using System;

namespace TangGame.Net
{
/// <summary>
/// Description of FBMsg.
/// </summary>
public class FBMsg
{
    /** 地图ID */
    public short mapId;
    /** 类型(1:单人FB 2:单人/多人FB 3:多人FB 4:全局FB 5:特殊活动场景) */
    public short type;
    /** 功能分类(1:普通FB 2:声望FB 3:历练FB 4:战场FB 5:其他活动FB) */
    public short fun;
    /** 活动ID(默认-1) */
    public int activityId;
    /** 副本难度 */
    public string difficulty;
    /** 进入最小等级 */
    public short minLevel;
    /** VIP功能附加进入次数 */
    public short vipFun;
    /** 进入所需物品基础ID(副本钥匙) */
    public int goodsId;
    /** 进入每次消耗疲劳 */
    public short consumeValue;
    /** 是否可以原地复活 */
    public bool isRelive;
}
}
