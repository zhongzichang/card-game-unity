using System;

namespace TangGame.Net
{
/// <summary>
/// 探索
/// </summary>
public class ExploreRecord
{
    /** 角色ID */
    public long heroId;
    /** 探索地图ID */
    public int exploreMapId;
    /** 所在位置 */
    public short nowX, nowY;
    /** 通过路径 */
    public short[][] openPath;
    /** 地图是否解锁 */
    public bool isUnlock;
    /** 是否领取奖励(true:已经领取 false:未领取) */
    public bool isGetReward;
}
}
