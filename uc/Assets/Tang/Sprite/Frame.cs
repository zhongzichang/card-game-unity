using System;
using UnityEngine;

namespace Tang {
	
	[Serializable]
	public class Frame {
		
		public Vector2 size;
		public Vector2 offset;
		public Vector2[] uv;
		
		public Frame(){
		}
		
		public Frame(Vector2 size, Vector2 offset, Vector2[] uv){
			this.size = size;
			this.offset = offset;
			this.uv = uv;
		}
	}

}